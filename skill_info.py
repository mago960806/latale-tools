def load_skill() -> pd.DataFrame:
    field_names = [
        "ID",
        "_Name",
        "_MaxSlv",
    ]
    df: pd.DataFrame = pd.read_csv(CSV_PATH / "SKILL.csv")
    df = df.loc[df["ID"] != 0]
    df = df[field_names]
    return df


def load_skill_use() -> pd.DataFrame:
    df: pd.DataFrame = pd.read_csv(CSV_PATH / "SKILL_USE.csv")
    df = df.loc[df["_Skill_ID"] != 0]
    return df


